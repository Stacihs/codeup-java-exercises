import java.util.Scanner;

public class StringExercise {
    public static void main(String[] args) {
        String line = "We don't need no education \nWe don't need no thought control";
        System.out.println(line);
        line = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(line);
        line = "In windows, the main drive is usually C:\\";
        System.out.println(line);
        line = "I can do backslashes \\, double backslashes \\\\,\n "+
        "and the amazing triple backslash \\\\\\!";
        System.out.println(line);
    }
}